num_dia_semana = int(input("Digite o número do dia da semana: "))

if num_dia_semana == 1:
   print("DOMINGO")
elif num_dia_semana == 2:
   print("SEGUNDA-FEIRA")
elif num_dia_semana == 3:
   print("TERÇA-FEIRA")
elif num_dia_semana == 4:
   print("QAURTA-FEIRA")
elif num_dia_semana == 5:
   print("QUINTA-FEIRA")
elif num_dia_semana == 6:
   print("SEXTA-FEIRA")
elif num_dia_semana == 7:
   print("SÁBADO")
else:
   print("VALOR INVÁLIDO!")