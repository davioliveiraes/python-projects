grau_celcius = int(input("Digite a temperatura em Celsius(ºC): "))
em_farenheit = grau_celcius * 1.8 + 32
em_kelvin = grau_celcius + 273

print(f"A temperatura atual é de: {grau_celcius}ºC, em Farenheit é de: {em_farenheit}ºF")
print(f"A temperatura atual é de: {grau_celcius}ºC, em Kelvin é de: {em_kelvin}K")
